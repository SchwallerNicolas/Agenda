package com.example.agenda;

public class Person {
    int id = 0;
    String name = null;
    String surname = null;

    public int getId() {
        return id;
    }
    public void setId(String name) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getSurname() {
        return surname;
    }
    public void setSurname(String surname) {
        this.surname = surname;
    }

}


